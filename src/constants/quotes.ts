// Theme-based quotes mapping
import type { ThemeId } from '@/config/themes';
import { getCurrentLanguage } from '@/i18n';
import { QUOTES_ES, CHRISTIAN_QUOTES_ES, STOIC_QUOTES_ES, MINIMALIST_QUOTES_ES } from './quotes.es';

const THEME_QUOTES: Record<ThemeId, Quote[]> = {
  default: QUOTES,
  christian: CHRISTIAN_QUOTES,
  stoic: STOIC_QUOTES,
  minimalist: MINIMALIST_QUOTES,
};

const THEME_QUOTES_ES: Record<ThemeId, Quote[]> = {
  default: QUOTES_ES,
  christian: CHRISTIAN_QUOTES_ES,
  stoic: STOIC_QUOTES_ES,
  minimalist: MINIMALIST_QUOTES_ES,
};

// Helper function to get quotes based on theme and language
export function getQuotes(themeId: ThemeId = 'default'): Quote[] {
  const lang = getCurrentLanguage();
  if (lang === 'es') {
    return THEME_QUOTES_ES[themeId] || QUOTES_ES;
  }
  return THEME_QUOTES[themeId] || QUOTES;
}

// Get a random quote for a theme
export function getRandomQuote(themeId: ThemeId = 'default'): Quote {
  const quotes = getQuotes(themeId);
  return quotes[Math.floor(Math.random() * quotes.length)];
}
